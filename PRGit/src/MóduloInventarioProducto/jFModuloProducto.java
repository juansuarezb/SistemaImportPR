   public void buscarProducto() {
    try {
        // Preparar la consulta para buscar un producto por su código
        PreparedStatement pps = cn.prepareStatement("SELECT * FROM inventario WHERE id_producto = ?");
        
        // Establecer el código del producto para buscarlo
        pps.setInt(1, Integer.parseInt(this.jTFCodigoProducto.getText())); // Suponiendo que jLAsiento contiene el código del producto a buscar

        // Ejecutar la consulta
        ResultSet rs = pps.executeQuery();

        // Verificar si se encontró el producto
        if (rs.next()) {
            // Obtener los atributos del producto encontrado
            String nombreProducto = rs.getString("nombre_producto");
            String descripcionProducto = rs.getString("descripcion_producto");
            BigDecimal costoProducto = rs.getBigDecimal("costo_producto");
            String categoriaProducto = rs.getString("categoria_producto");
            int stockProducto = rs.getInt("stock_producto");
            String ubicacionProducto = rs.getString("ubicacion_producto");
            
            // Concatenar toda la información del producto en una cadena
            String informacionProducto = "Nombre del producto: " + nombreProducto + "\n" +
                                          "Descripción del producto: " + descripcionProducto + "\n" +
                                          "Costo del producto: " + costoProducto + "\n" +
                                          "Categoría del producto: " + categoriaProducto + "\n" +
                                          "Stock del producto: " + stockProducto + "\n" +
                                          "Ubicación del producto: " + ubicacionProducto;

            // Mostrar la información del producto en un nuevo JFrame
            VentanaProducto ventanaProducto = new VentanaProducto(informacionProducto);
            ventanaProducto.setVisible(true);

        } else {
            System.out.println("El producto no fue encontrado.");
        }

        // Cerrar recursos
        rs.close();
        pps.close();
    } catch (SQLException ex) {
        System.out.println(ex);
    }
} 
    
