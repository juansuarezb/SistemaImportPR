    private void btnGuardarCliente3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCliente3ActionPerformed
    String codigoP = this.jTFIDProducto.getText();
    String nombreP = this.jTFNombreRegistrar.getText();
    String descripcionP = this.jTFDescripcionRegistrar.getText();
    String costoP = this.jTFCostoRegistrar.getText();
    String categoriaP = this.jTFCategoriaRegistrar.getText();
    String stockP = this.jTFStockRegistrar.getText();
    String ubicacionP = this.jTFUbicacionRegistrar.getText();
    
    // Realizar validaciones
    if (codigoP.isEmpty() || nombreP.isEmpty() || descripcionP.isEmpty() || costoP.isEmpty() || categoriaP.isEmpty() || stockP.isEmpty() || ubicacionP.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Detener la ejecución del método si falta algún dato
    }
    
    try {
        // Validar que el costo sea un número válido
        double costo = Double.parseDouble(costoP);
        if (costo <= 0) {
            JOptionPane.showMessageDialog(this, "El costo debe ser un número positivo", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Detener la ejecución del método si el costo no es válido
        }
        
        // Validar que el stock sea un número válido
        int stock = Integer.parseInt(stockP);
        if (stock < 0) {
            JOptionPane.showMessageDialog(this, "El stock debe ser un número positivo o cero", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Detener la ejecución del método si el stock no es válido
        }
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "El costo y el stock deben ser números válidos", "Error", JOptionPane.ERROR_MESSAGE);
        return; // Detener la ejecución del método si el costo o el stock no son números válidos
    }
    
    // Si todas las validaciones pasan, crear el objeto Producto y registrar
    Producto producto = new Producto(codigoP, nombreP, descripcionP, Double.parseDouble(costoP), categoriaP, Integer.parseInt(stockP), ubicacionP);
    producto.registrarProducto(cn, codigoP, nombreP, descripcionP, costoP, categoriaP, stockP, ubicacionP);
    mostrar("inventario");
    }//GEN-LAST:event_btnGuardarCliente3ActionPerformed

